package org.pw.foodordering.elements.misc;

public class SizeContainer
{
  Integer newOrdersSize;
  Integer oldOrderSize;
  
  public Integer getNewOrdersSize()
  {
    return this.newOrdersSize;
  }
  
  public void setNewOrdersSize(Integer newOrdersSize)
  {
    this.newOrdersSize = newOrdersSize;
  }
  
  public Integer getOldOrderSize()
  {
    return this.oldOrderSize;
  }
  
  public void setOldOrderSize(Integer oldOrderSize)
  {
    this.oldOrderSize = oldOrderSize;
  }
}
