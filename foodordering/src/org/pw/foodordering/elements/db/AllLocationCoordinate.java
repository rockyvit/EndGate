package org.pw.foodordering.elements.db;

public class AllLocationCoordinate {
	Integer alc_id;
	Double location_logitude;
	Double location_latitude;
	String location_name;
	
	
	public Integer getAlc_id() {
		return alc_id;
	}
	public void setAlc_id(Integer alc_id) {
		this.alc_id = alc_id;
	}
	public Double getLocation_logitude() {
		return location_logitude;
	}
	public void setLocation_logitude(Double location_logitude) {
		this.location_logitude = location_logitude;
	}
	public Double getLocation_latitude() {
		return location_latitude;
	}
	public void setLocation_latitude(Double location_latitude) {
		this.location_latitude = location_latitude;
	}
	public String getLocation_name() {
		return location_name;
	}
	public void setLocation_name(String location_name) {
		this.location_name = location_name;
	}
	

}
